void Map_Draw() {
  int type = 0;

  for (int row = 0; row < 20; row++) {
    for (int col = 0; col < 25; col++) {
      type = Map_Tiles[row][col];

      Map_Dest.x = col * 32;
      Map_Dest.y = row * 32;

      switch (type) {
      case 0:
        TM_Draw(Water, Map_Src, Map_Dest);
        break;
      case 1:
        TM_Draw(Grass, Map_Src, Map_Dest);
        break;
      case 2:
        TM_Draw(Dirt, Map_Src, Map_Dest);
        break;
      default:
        break;
      }
    }
  }
};
